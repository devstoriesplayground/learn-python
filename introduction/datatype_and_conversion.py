# data type specify the type of value in a variable can be store and the type of operation can be perform.

# string are consist of characters that contain in single or double quote
var = "hello world"
print(var)

# integer are zero, positive or negative whole numbers without a fractional part
num = 160000
print(num)

# float are positive and negative real numbers with a fractional part denoted by the decimal symbol
num = 1.65
print(num)

# boolean are either true or false
var = True
var_int = (100 == 7)
print(var)
print(var_int)


# int() function converts a float into int
var = 10.50
print(int(var))

# float() function converts a int into float
var = 100
print(float(var))

# str() function converts a int into string
var = 1000
print(str(var))

# tuple() function convert a string into tuple
var = "devstories playground"
print(tuple(var))

# list() function convert a string to list
var = "devstories playground"
print(list(var))
